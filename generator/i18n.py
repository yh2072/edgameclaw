def extract_locale_content(config: dict, script: list) -> dict:
    """Extract all translatable content from a generated game into a locale JSON structure.

    This JSON can later be sent to an AI for translation, producing a new locale
    file that the engine loads at runtime to switch languages.
    """
    locale = config.get("locale", DEFAULT_LOCALE)

    meta = {
        "title": config.get("title", ""),
        "subtitle": config.get("subtitle", ""),
        "description": config.get("description", ""),
        "defaultPlayerName": config.get("defaultPlayerName", ""),
    }

    characters = {}
    for cid, cinfo in config.get("characters", {}).items():
        if isinstance(cinfo, dict) and "name" in cinfo:
            characters[cid] = cinfo["name"]

    dialogs = []
    for i, cmd in enumerate(script):
        if isinstance(cmd, dict) and cmd.get("type") == "dialog":
            dialogs.append([i, cmd.get("name", ""), cmd.get("text", "")])

    minigames_stripped = {}
    for mg_key, mg_data in config.get("minigames", {}).items():
        if isinstance(mg_data, dict):
            minigames_stripped[mg_key] = _strip_non_text(mg_data)

    end_screen = {}
    es_data = config.get("endScreen", {})
    if es_data.get("mechanics"):
        end_screen["mechanics"] = [
            {"mechanic": m.get("mechanic", ""), "knowledge": m.get("knowledge", "")}
            for m in es_data["mechanics"]
        ]

    ui = config.get("ui", get_ui_strings(locale))

    return {
        "locale": locale,
        "meta": meta,
        "characters": characters,
        "dialogs": dialogs,
        "minigames": minigames_stripped,
        "endScreen": end_screen,
        "ui": ui,
    }


def _strip_non_text(obj):
    """Deep-copy a minigame data dict, keeping only text-bearing fields."""
    if isinstance(obj, str):
        return obj
    if isinstance(obj, list):
        return [_strip_non_text(item) for item in obj]
    if isinstance(obj, dict):
        return {k: _strip_non_text(v) for k, v in obj.items()}
    return obj
